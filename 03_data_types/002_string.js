let hello = 'Hello!.';

console.log("---- hello is: ", hello);
console.log(`---- hello is: ${hello}`);
console.log("---- typeof hello is: ", typeof hello);
console.log("---- hello.length is: ", hello.length);
console.log("---- hello.toUpperCase() is: ", hello.toUpperCase());
console.log("---- hello.toLowerCase() is: ", hello.toLowerCase());

console.log("---- 'Keyword'==='Keyword' is: ", 'Keyword'==='Keyword');
console.log("---- 'Keyword'==='keyword' is: ", 'Keyword'==='keyword');
console.log("---- 'Keyword'.toLowerCase()==='keyword' is: ", 'Keyword'.toLowerCase()==='keyword');